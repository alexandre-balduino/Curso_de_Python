'''
Desafio 006

Crie um algoritmo que leia um número 
e mostre o seu dobro, triplo e 
a raiz quadrada.
'''

num = int(input("Digite um número: "))
dob = num * 2
tri = num * 3
raiz = num ** 0.5
print(f"O dobro de {num} é {dob}")
print(f"O triplo de {num} é {tri}")
print(f"E a raiz quadrada de {num} é {raiz}")
